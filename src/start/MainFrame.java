    private void EditTestButtonActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_EditTestButtonActionPerformed
    {//GEN-HEADEREND:event_EditTestButtonActionPerformed
        // TODO add your handling code here:
        JFileChooser fc = new JFileChooser();

        fc.removeChoosableFileFilter(fc.getFileFilter());
        fc.setFileFilter(new FileNameExtensionFilter("Question File", "q"));
        fc.setDialogTitle("Select Question file to Edit");
        if (fc.showOpenDialog(this) != JFileChooser.APPROVE_OPTION)
        {
            return;
        }

        File q = fc.getSelectedFile();
        JFileChooser fc2 = new JFileChooser();
        fc.removeChoosableFileFilter(fc.getFileFilter());
        fc2.setFileFilter(new FileNameExtensionFilter("Answer File", "a"));
        fc2.setDialogTitle("Select Answer file to Edit");
        if (fc2.showOpenDialog(this) != JFileChooser.APPROVE_OPTION)
        {
            return;
        }
        File a = fc2.getSelectedFile();


        Test Q, A;
        ObjectInputStream in;
        try
        {
            in = new ObjectInputStream(new FileInputStream(q));
            Q = (Test) in.readObject();
            in = new ObjectInputStream(new FileInputStream(a));
            A = (Test) in.readObject();
            if (Q.Name.equals(A.Name) && Q.Course.equals(A.Course) &&
                    Q.SubName.equals(A.SubName) && Q.SubCode.equals(
                    A.SubCode) && Q.DateOfTest.equals(A.DateOfTest) &&
                    Q.NoOfSections == A.NoOfSections)
            {
                Question.Dir = q.getParentFile();
                QuestionInputFrame QIF = new QuestionInputFrame();
                QIF.loadFile(Q, A);
                QIF.setVisible(true);
                this.dispose();
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Wrong file selected");
            }

        } catch (IOException ex)
        {
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex)
        {
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
}//GEN-LAST:event_EditTestButtonActionPerformed

