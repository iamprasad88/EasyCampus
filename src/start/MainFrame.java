    private void CreateTestButtonActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_CreateTestButtonActionPerformed
    {//GEN-HEADEREND:event_CreateTestButtonActionPerformed
        // TODO add your handling code here:
        JFileChooser FC = new JFileChooser(Question.Dir);
        FC.setFileSelectionMode(FC.DIRECTORIES_ONLY);
        FC.setDialogTitle("Select Test Directory");
        if (FC.showOpenDialog(this) == JFileChooser.APPROVE_OPTION)
        {
            File file = FC.getSelectedFile();
            if (file.isDirectory())
            {
                Question.Dir = file;
                JOptionPane.showMessageDialog(null,
                        "Please Move all the necessary media files to : \n" + file.getAbsolutePath(),
                        "Important!!!", JOptionPane.INFORMATION_MESSAGE);
                StartCreate ob = new StartCreate();
                ob.setVisible(true);
                this.dispose();
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Select Dir failed", "Error!!!",
                        JOptionPane.ERROR_MESSAGE);
            }
        }

}//GEN-LAST:event_CreateTestButtonActionPerformed

    private void TakeTestButtonActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_TakeTestButtonActionPerformed
    {//GEN-HEADEREND:event_TakeTestButtonActionPerformed
        // TODO add your handling code here:
        JFileChooser fc = new JFileChooser();
        fc.removeChoosableFileFilter(fc.getFileFilter());
        fc.setDialogTitle("Open a Question File");
        File testFile;
        fc.setFileFilter(new FileNameExtensionFilter("Question File", "q"));
        int op = fc.showOpenDialog(this);
        if (op == JFileChooser.APPROVE_OPTION)
        {
            testFile = fc.getSelectedFile();
            Question.setDir(testFile.getParentFile());
            if (testFile.exists())
            {
                try
                {
                    ObjectInputStream in =
                            new ObjectInputStream(new FileInputStream(testFile));
                    Test Questions = (Test) in.readObject();
                    File Ans = Questions.getRSP();

                    if (Ans.exists() == false)
                    {
                        Ans.mkdirs();
                    }
                    TestDetailsFrame t = new TestDetailsFrame(Questions);
                    t.setVisible(true);
                    this.dispose();
                } catch (IOException ex)
                {
                    Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ClassNotFoundException ex)
                {
                    Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
                }

            }
            else
            {
                JOptionPane.showMessageDialog(this, "File Does not Exist", "File Error!!!",
                        JOptionPane.ERROR_MESSAGE);
            }
        }

}//GEN-LAST:event_TakeTestButtonActionPerformed

